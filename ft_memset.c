#include"libft.h"

void	*ft_memset( void *b, int c, size_t len)
{
	size_t	i;
	char	*sr;

	i = 0;
	sr = b;
	while (i < len)
	{
		sr[i] = c;
		i++;
	}
	return (sr);
}
